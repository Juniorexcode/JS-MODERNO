
let score = 5;

score++; // 5, y cuando lo vuelves a llamar, se incrementa en 1
++score; // 6, se incrementa en 1 desde el inicio

score--; // 5, y cuando lo vuelves a llamar, se decrementa en 1
--score; // 4, se decrementa en 1 desde el inicio

score += 5; // 10, se incrementa en 5
score -= 5; // 0, se decrementa en 5

console.log(`Score: ${score}`);

